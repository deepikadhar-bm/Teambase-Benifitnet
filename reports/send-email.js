// ============================================================================
//  SEND EMAIL
//  Generates the report ZIP and optionally sends it via Gmail.
//
//  Required .env variables:
//    EMAIL_USER=your.gmail@gmail.com
//    EMAIL_PASS=your-app-password       ← Gmail App Password (not account password)
//    EMAIL_TO=recipient@example.com     ← comma-separated for multiple recipients
//    AUTO_SEND_EMAIL=true               ← set true to auto-send after every run
//    PROJECT_NAME=Teambase-BenefitNet   ← optional, defaults to package.json name
//
//  Gmail App Password setup:
//    1. Go to https://myaccount.google.com/security
//    2. Enable 2-Step Verification
//    3. Go to App Passwords → create one for "Mail"
//    4. Paste the 16-char password as EMAIL_PASS in .env
// ============================================================================

require("dotenv").config({ path: require("path").join(__dirname, "../.env") });

const path = require("path");
const nodemailer = require("nodemailer");
const { generateReport } = require(path.join(__dirname, "./generate-report"));

// ── Helpers ──────────────────────────────────────────────────────────────────

function getProjectName() {
  if (process.env.PROJECT_NAME) return process.env.PROJECT_NAME;
  try {
    return require(path.join(__dirname, "../package.json")).name || "qa-project";
  } catch {
    return "qa-project";
  }
}

function getReadableTimestamp() {
  return new Date().toLocaleString("en-IN", {
    timeZone: "Asia/Kolkata",
    dateStyle: "full",
    timeStyle: "medium",
  });
}

