// ── Main function ─────────────────────────────────────────────────────────────

/**
 * Generate ZIP and optionally send email.
 * @param {boolean} sendNow  true = send email, false = ZIP only
 */
async function sendEmail(sendNow = true) {
  const projectName = getProjectName();

  // ── Step 1: Always generate the ZIP ─────────────────────────────────────
  // console.log(`\n  Generating report ZIP for: ${projectName}`);
  const { zipFileName, zipPath } = await generateReport(projectName);

  // ── Step 2: Skip email if not requested ──────────────────────────────────
  if (!sendNow) {
    // console.log(`  Email Mode    : MANUAL`);
    // console.log(`  To send now   : node reports/send-email.js`);
    return;
  }

  // ── Step 3: Validate .env credentials ───────────────────────────────────
  const { EMAIL_USER, EMAIL_PASS, EMAIL_TO } = process.env;

  if (!EMAIL_USER || !EMAIL_PASS || !EMAIL_TO) {
    const missing = [
      !EMAIL_USER ? "EMAIL_USER" : null,
      !EMAIL_PASS ? "EMAIL_PASS" : null,
      !EMAIL_TO ? "EMAIL_TO" : null,
    ].filter(Boolean).join(", ");

    throw new Error(
      `Missing .env variables: ${missing}\n` +
      `Add them to your .env file at the project root:\n` +
      `  EMAIL_USER=your.gmail@gmail.com\n` +
      `  EMAIL_PASS=your-16-char-app-password\n` +
      `  EMAIL_TO=recipient@example.com`
    );
  }

  // ── Step 4: Send via Gmail SMTP ──────────────────────────────────────────
  const timestamp = getReadableTimestamp();
  const subject = `[QA Report] ${projectName} — Playwright Execution Report`;
  const htmlBody = buildEmailBody(projectName, zipFileName, timestamp);
  const recipients = EMAIL_TO.split(",").map(e => e.trim()).filter(Boolean);

  const transporter = nodemailer.createTransport({
    service: "gmail",
    auth: {
      user: EMAIL_USER,
      pass: EMAIL_PASS,
    },
  });

  await transporter.sendMail({
    from: `"QA Automation" <${EMAIL_USER}>`,
    to: recipients.join(", "),
    subject,
    html: htmlBody,
    attachments: [{ filename: zipFileName, path: zipPath }],
  });

  // console.log(`  Email Sent    : ${recipients.join(", ")}`);
  // console.log(`  Subject       : ${subject}`);
}

module.exports = sendEmail;

// Allow direct execution: node reports/send-email.js
if (require.main === module) {
  sendEmail(true)
    .then(() => { }) // console.log("\n  Done.\n")
    .catch(err => {
      console.error("\n[send-email] Error:", err.message || err);
      process.exit(1);
    });
}